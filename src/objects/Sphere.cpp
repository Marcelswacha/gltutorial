Sphere::Sphere(const ObjectProperties& props)
  : RenderObject(props)
{
  // generate and bind VAO
  glGenVertexArrays(1, &vertexArrayObject);
  glBindVertexArray(vertexArrayObject);

  // generate, bind and copy VBO
  GLuint vbo;
  glGenBuffers(1, &vbo);
  glBindBuffer(GL_ARRAY_BUFFER, vbo);
  glBufferData(GL_ARRAY_BUFFER, sizeof(float) * 26 * 26 * 8, properties.vertexData, GL_STATIC_DRAW);

  // copy indices
  GLuint ebo;
  glGenBuffers(1, &ebo);
  glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, ebo);
  glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(float) * 1200, properties.vertexIndices, GL_STATIC_DRAW);

  // set the vertex attributes pointers
  //position
  glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
  glEnableVertexAttribArray(0);

  glBindBuffer(GL_ARRAY_BUFFER, 0);
  glBindVertexArray(0);
}

void Sphere::draw(const SceneInfo& info)
{
  Shader* shader = properties.shader;
  shader->use();

  glm::mat4 model = glm::mat4(1.0f);
  model = glm::translate(model, properties.position);
  model = glm::scale(model, glm::vec3{0.1, 0.1, 0.1});

  // 3d scene
  shader->setMat4("model", model);
  shader->setMat4("view", info.viewMatrix);
  shader->setMat4("projection", info.projectionMatrix);

  glBindVertexArray(vertexArrayObject);
  glDrawElements(GL_TRIANGLES, 1200, GL_UNSIGNED_INT, (void*)0);
}
